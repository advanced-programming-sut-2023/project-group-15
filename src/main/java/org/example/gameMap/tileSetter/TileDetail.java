package org.example.gameMap.tileSetter;

public class TileDetail {
    public double x;
    public double y;

    public TileDetail(double x,double y ) {
        this.x = x;
        this.y = y;
    }

    public void setCoordinate(double x, double y) {
        this.x = x;
        this.y = y;
    }

}
